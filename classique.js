// Fonction pour afficher les citations
function displayCitations() {
    const select = document.getElementById("selectAuthor");
    const author = select.value;
    const citationContainer = document.getElementById("citationContainer");
    citationContainer.innerHTML = ""; // Vide le contenu précédent

    if (author) {
        const authorCitations = citations[author];

        if (authorCitations) {
            authorCitations.forEach((citationObj, index) => {
                // Créer un paragraphe pour la citation
                const p = document.createElement("p");
                p.textContent = `"${citationObj.citation}" - ${citationObj.source}`;
                citationContainer.appendChild(p);
                
                // Ajouter un saut de ligne après chaque citation sauf la dernière
                if (index < authorCitations.length - 1) {
                    const br = document.createElement("br");
                    citationContainer.appendChild(br);
                }
            });
        } else {
            citationContainer.innerHTML = "<p>Aucune citation disponible pour cet auteur/artiste.</p>";
        }
    }
}
